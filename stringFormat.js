// Declare three strings in different formats
const string1 = "Hello, World!";  // Double quotes
const string2 = 'JavaScript is fun';  // Single quotes
const string3 = `This is a string with template literals`;  // Template literals

// Find the length of each string
const length1 = string1.length;  // Length of string1
const length2 = string2.length;  // Length of string2
const length3 = string3.length;  // Length of string3

// Print the lengths
console.log(`Length of string1 ("${string1}") is: ${length1}`);
console.log(`Length of string2 ('${string2}') is: ${length2}`);
console.log(`Length of string3 (\`${string3}\`) is: ${length3}`);
